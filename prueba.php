const d = document;
export default function sliderAdd(){
    const carrusell = d.querySelector(".carrusell");
    let step = 1;
    let intervalo = null;
    const start = ()=>{
        intervalo = setInterval(function () {
       
            let maxScrollLeft = carrusell.scrollWidth - carrusell.clientWidth;
           
            // carrusel-scrollleft desde el comienzo  es 0, porque no  se ha desplazado nada hacia la izquierda.  a eso le sumo el mismo mas 1 pixel
            carrusell.scrollLeft = carrusell.scrollLeft + step;
            //console.log(boxAdds.scrollLeft)
            //si el pedazo que esta oculto de la derecha   ya es igual a el pedazo oculto de el carrusel pero de la izquierda,  invierto y  muevo  hacia la derecha.
            //console.log(carrusell.scrollLeft)
            //console.log(boxIntercepted.length)
            //ese ifes se ejecutan solo una vez, es decir solo cuando el carrusell llega al limite
            if (carrusell.scrollLeft === maxScrollLeft) {
                step = step * -1;
              
                } else if (carrusell.scrollLeft === 0) {
        
                step = step * -1;
                }
        },20);
    }
    
    const stop = () => {
        clearInterval(intervalo);
      };
      carrusell.addEventListener("mouseover", () => {
        stop();
      });
      carrusell.addEventListener("mouseout", () => {
        start();
      });
      start();
}





